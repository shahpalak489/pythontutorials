## Method 1 (Using Arithmetic Operators)
x = 10
y = 5
  
# x now becomes 15 
x = x + y   # x= 15
  
# y becomes 10 
y = x - y  # y = 10
  
# x becomes 5 
x = x - y   # x = 5
print("After Swapping: x =", x, " y =", y)



